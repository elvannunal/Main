public class MathSınıfıveMetotları {
    public static void main(String[] args) {
        System.out.println(Math.abs(-100));//mutlak değer
        System.out.println(Math.ceil(1.00001));//bir üsse yuvarlamak için kull.
        System.out.println(Math.floor(1.00001));//bir alt değere yuvarlar
        System.out.println(Math.max(100,300));//x,y değeleri var
        System.out.println(Math.pow(2,3));//Üssü hesabı yapar
        System.out.println(Math.random()*10);//neyle çarparsan 0 la o değer arasında random bir değer üretir
        System.out.println(Math.sqrt(4));//karekçk hesabı yapar
    }
}
